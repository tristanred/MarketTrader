#!/usr/bin/env node
/**
 * Generates docs/achievements.md and docs/achievements/img/<key>.png for every
 * achievement in the server's catalog.
 *
 * Requirements (running on developer's machine, all already part of the dev loop):
 *   - The backend is running on PORT (default 3000).
 *   - The frontend dev server is running on FRONTEND_PORT (default 5173).
 *     Both are started by `pnpm dev` at repo root.
 *   - An admin account exists. The script POSTs /auth/login with the credentials
 *     in DOCS_ADMIN_USER / DOCS_ADMIN_PASSWORD (defaults: tristan / abcd1234).
 *
 * The script:
 *   1) Logs in as admin and pulls the achievement catalog from GET /admin/achievements.
 *   2) Launches a headless Chromium with prefers-reduced-motion forced, so the
 *      toast's CSS keyframes collapse to ~0ms and the rendered output is the
 *      animation's final state. No timing race.
 *   3) For each achievement, navigates to /__toast-preview with the metadata in
 *      query params and screenshots the toast element into the docs/img directory.
 *   4) Writes a single docs/achievements.md grouped by category, with the
 *      preview image and the existing description/rarity/target inline.
 *
 * Run with: pnpm docs:achievements (from repo root).
 */

// `@playwright/test` ships its own browser bundle and is already a devDep here.
import { chromium } from '@playwright/test';
import { mkdir, writeFile, rm } from 'node:fs/promises';
import { existsSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(__dirname, '..', '..', '..');

const API_BASE = process.env.DOCS_API_BASE ?? 'http://localhost:3000';
const FRONTEND_BASE = process.env.DOCS_FRONTEND_BASE ?? 'http://localhost:5173';
const ADMIN_USER = process.env.DOCS_ADMIN_USER ?? 'tristan';
const ADMIN_PASSWORD = process.env.DOCS_ADMIN_PASSWORD ?? 'abcd1234';

const DOCS_DIR = join(repoRoot, 'docs');
const IMG_DIR = join(DOCS_DIR, 'achievements', 'img');
const MD_PATH = join(DOCS_DIR, 'achievements.md');

const CATEGORY_ORDER = ['trading', 'pnl', 'portfolio', 'standing', 'behavior', 'finale'];
const CATEGORY_LABELS = {
  trading: 'Trading',
  pnl: 'P&L',
  portfolio: 'Portfolio',
  standing: 'Standing',
  behavior: 'Behavior',
  finale: 'Finale',
};
const RARITY_ORDER = { legendary: 0, epic: 1, rare: 2, uncommon: 3, common: 4 };

async function login() {
  const res = await fetch(`${API_BASE}/auth/login`, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ username: ADMIN_USER, password: ADMIN_PASSWORD }),
  });
  if (!res.ok) {
    throw new Error(`Login failed (${res.status}): set DOCS_ADMIN_USER / DOCS_ADMIN_PASSWORD if your admin isn't '${ADMIN_USER}'`);
  }
  const body = await res.json();
  if (!body.user.groups.includes('admin')) {
    throw new Error(`User '${ADMIN_USER}' is not an admin`);
  }
  return body.token;
}

async function fetchCatalog(token) {
  const res = await fetch(`${API_BASE}/admin/achievements`, {
    headers: { Authorization: `Bearer ${token}` },
  });
  if (!res.ok) {
    throw new Error(`GET /admin/achievements failed (${res.status})`);
  }
  const body = await res.json();
  return body.definitions;
}

async function captureToast(page, def, outputPath) {
  const params = new URLSearchParams({
    name: def.name,
    description: def.description,
    rarity: def.rarity,
    icon: def.icon,
  });
  await page.goto(`${FRONTEND_BASE}/__toast-preview?${params.toString()}`, {
    waitUntil: 'networkidle',
  });
  const toast = await page.waitForSelector('[role="status"]', { timeout: 5_000 });
  // Wait one frame so the reduced-motion-collapsed animations have applied.
  await page.evaluate(() => new Promise((r) => requestAnimationFrame(() => requestAnimationFrame(r))));
  await toast.screenshot({ path: outputPath, omitBackground: true });
}

function renderMarkdown(catalog) {
  const byCategory = new Map();
  for (const def of catalog) {
    const cat = def.category ?? 'other';
    if (!byCategory.has(cat)) byCategory.set(cat, []);
    byCategory.get(cat).push(def);
  }
  for (const list of byCategory.values()) {
    list.sort((a, b) => {
      const r = (RARITY_ORDER[a.rarity] ?? 99) - (RARITY_ORDER[b.rarity] ?? 99);
      if (r !== 0) return r;
      return a.name.localeCompare(b.name);
    });
  }

  const enabledCount = catalog.filter((d) => d.enabled).length;
  const disabledCount = catalog.length - enabledCount;

  const lines = [];
  lines.push('# Achievements');
  lines.push('');
  lines.push('> Auto-generated by `pnpm docs:achievements`. Do not edit by hand —');
  lines.push('> re-run the generator after adding or modifying an achievement.');
  lines.push('');
  lines.push(`${catalog.length} achievements total — ${enabledCount} enabled, ${disabledCount} disabled.`);
  lines.push('');
  lines.push('## Table of contents');
  lines.push('');
  for (const cat of CATEGORY_ORDER) {
    if (!byCategory.has(cat)) continue;
    lines.push(`- [${CATEGORY_LABELS[cat] ?? cat}](#${cat})`);
  }
  for (const cat of byCategory.keys()) {
    if (!CATEGORY_ORDER.includes(cat)) {
      lines.push(`- [${cat}](#${cat})`);
    }
  }
  lines.push('');

  const orderedCats = [
    ...CATEGORY_ORDER.filter((c) => byCategory.has(c)),
    ...Array.from(byCategory.keys()).filter((c) => !CATEGORY_ORDER.includes(c)),
  ];
  for (const cat of orderedCats) {
    const label = CATEGORY_LABELS[cat] ?? cat;
    lines.push(`## ${label}`);
    lines.push('');
    for (const def of byCategory.get(cat)) {
      lines.push(`### ${def.name}`);
      lines.push('');
      lines.push(`![${def.name}](./achievements/img/${def.key}.png)`);
      lines.push('');
      lines.push(`${def.description}`);
      lines.push('');
      lines.push('| Field | Value |');
      lines.push('| --- | --- |');
      lines.push(`| Key | \`${def.key}\` |`);
      lines.push(`| Rarity | ${capitalize(def.rarity)} |`);
      lines.push(`| Icon | \`${def.icon}\` |`);
      lines.push(`| Target | ${def.target} |`);
      lines.push(`| Enabled | ${def.enabled ? 'yes' : 'no (globally disabled)'} |`);
      lines.push('');
    }
  }

  return lines.join('\n');
}

function capitalize(s) {
  return s.charAt(0).toUpperCase() + s.slice(1);
}

async function main() {
  console.log(`Logging in as ${ADMIN_USER}…`);
  const token = await login();
  console.log('Fetching achievement catalog…');
  const catalog = await fetchCatalog(token);
  console.log(`  → ${catalog.length} achievements`);

  // Wipe + recreate the image directory so deleted achievements don't leave
  // orphan PNGs behind.
  if (existsSync(IMG_DIR)) await rm(IMG_DIR, { recursive: true });
  await mkdir(IMG_DIR, { recursive: true });

  console.log('Launching Chromium with prefers-reduced-motion forced…');
  const browser = await chromium.launch();
  const context = await browser.newContext({
    reducedMotion: 'reduce',
    viewport: { width: 600, height: 200 },
    deviceScaleFactor: 2,
  });
  const page = await context.newPage();

  let i = 0;
  for (const def of catalog) {
    i += 1;
    const outputPath = join(IMG_DIR, `${def.key}.png`);
    console.log(`  [${i}/${catalog.length}] ${def.name} (${def.rarity}) → ${def.key}.png`);
    await captureToast(page, def, outputPath);
  }

  await browser.close();

  console.log('Writing markdown…');
  await writeFile(MD_PATH, renderMarkdown(catalog), 'utf8');
  console.log(`✔ ${MD_PATH}`);
  console.log(`✔ ${IMG_DIR}/`);
}

main().catch((err) => {
  console.error(err);
  process.exitCode = 1;
});
